    private void loadDataBuku() {
        model.setRowCount(0);
        for (Book b : service.getAllBooks()) {
            model.addRow(new Object[]{
                b.getIdBuku(),
                b.getJudul(),
                b.getPenulis(),
                b.getTahunTerbit()
            });
        }
    }

    private Book cariBuku(String input) {
        for (Book b : service.getAllBooks()) {
            if (b.getIdBuku().equalsIgnoreCase(input)
                    || b.getJudul().equalsIgnoreCase(input)) {
                return b;
            }
        }
        return null;
    }

