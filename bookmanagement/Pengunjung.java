    private void PinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PinjamActionPerformed
        // TODO add your handling code here:
        String inputBuku = txtIdBuku.getText().trim(); // bisa ID atau Judul
        String namaPeminjam = txtNamaPeminjam.getText().trim();
        String nomorPeminjam = txtNomorPeminjam.getText().trim();

        // 1. Validasi input
        if (inputBuku.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "ID Buku atau Judul Buku harus diisi!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (namaPeminjam.isEmpty() || nomorPeminjam.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Nama dan Nomor Peminjam harus diisi!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Cari buku
        Book buku = cariBuku(inputBuku);

        if (buku == null) {
            JOptionPane.showMessageDialog(this,
                    "Buku tidak ditemukan!",
                    "Gagal",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Jika berhasil
        JOptionPane.showMessageDialog(this,
                "Peminjaman Berhasil!\n\n"
                + "Judul Buku : " + buku.getJudul() + "\n"
                + "Peminjam   : " + namaPeminjam + "\n"
                + "Nomor      : " + nomorPeminjam,
                "Informasi",
                JOptionPane.INFORMATION_MESSAGE);

        // 4. Optional: reset field
        txtIdBuku.setText("");
        txtNamaPeminjam.setText("");
        txtNomorPeminjam.setText("");
    }//GEN-LAST:event_PinjamActionPerformed

